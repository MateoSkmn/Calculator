using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Calculation : MonoBehaviour
{

    private void Start()
    {
        print(add(4, 3));
        print(sub(10, 4);
    }

    int sub(int a, int b)
    {
        return a - b;
    }

    int add(int fst, int snd)
    {
        return fst + snd;
    }
}
